#include <iostream>

int main() {
// INT
    int calkowita = 15;
    
    std::cout << "10 + 5 = " << 10 + 5 << '\n';
    std::cout << "10 - 3 = " << 10 - 3 << '\n';
    std::cout << "7 * 3 = " << 7 * 3 << '\n';
    std::cout << "5 / 2 = " << 5 / 2 << '\n'; // dzielenie dwóch intów zawsze zwraca int (część całkowitą z dzielenia
    std::cout << "5 % 2 = " << 5 % 2 << '\n'; //modulo - reszta z dzielenia
    
    std::cout << "calkowita - 3 = " << calkowita - 3 << '\n';
    std::cout << calkowita << " - 3 = " << calkowita - 3 << '\n';
    
}
